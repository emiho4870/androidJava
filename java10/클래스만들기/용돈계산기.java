package 클래스만들기;

public class 용돈계산기 {
	
	
	public int mul(int x , int y) {
		int result = x * y;
		System.out.println(result + "용돈계산기:main메서드:mul");
		return result;
	}
	
	public int add(int x , int y) {
		int result = x + y;
		System.out.println(result + "용돈계산기:main메서드:add");
		return result;
	}
	

	

}
